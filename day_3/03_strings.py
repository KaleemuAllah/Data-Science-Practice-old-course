#Type casting is a mechanism used in Python and other programming languages 
# to convert a variable of one data type into another.
# In Python, we use type casting by calling the type function followed by the 
# variable or expression that we want to convert. Here's a basic example:

# python
# Copy code
# var1 = "50"
# var2 = int(var1) # converting string to integer
# var3 = float(var1) # converting string to float

# print(type(var1)) # Output: <class 'str'>
# print(type(var2)) # Output: <class 'int'>
# print(type(var3)) # Output: <class 'float'>
